    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedIndex = jComboBoxItemPesanan.getSelectedIndex();
        if (selectedIndex < 0 || itemsToDisplay.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih item pesanan terlebih dahulu.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        DetailPesanan detailToDelete = itemsToDisplay.get(selectedIndex);

        int confirm = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menghapus item: " + detailToDelete.getMenu().getNama() + " x" + detailToDelete.getKuantitas() + "?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            currentPesanan.hapusDetailPesanan(detailToDelete);

            JOptionPane.showMessageDialog(this, 
                                          "Pesanan " + detailToDelete.getMenu().getNama() + " berhasil dihapus.", 
                                          "Pesanan Dihapus", 
                                          JOptionPane.INFORMATION_MESSAGE);
            populateComboBox(); 

            if (currentPesanan.getDetailPesananList().isEmpty()) {
                this.dispose(); 
            } else {
                 if(jComboBoxItemPesanan.getItemCount() > 0) {
                     jComboBoxItemPesanan.setSelectedIndex(0);
                 }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

