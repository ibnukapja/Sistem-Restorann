    /**
     * @param args the command line arguments
     */
    
    private void populateComboBox() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        itemsToDisplay = new java.util.ArrayList<>();

        if (currentPesanan != null && !currentPesanan.getDetailPesananList().isEmpty()) {
            for (DetailPesanan detail : currentPesanan.getDetailPesananList()) {
                model.addElement(String.format("%s x%d = Rp%,.0f", 
                                               detail.getMenu().getNama(), 
                                               detail.getKuantitas(), 
                                               detail.getSubTotal()));
                itemsToDisplay.add(detail);
            }
            jComboBoxItemPesanan.setModel(model);
            jComboBoxItemPesanan.setSelectedIndex(0);
        } else {
            model.addElement("Tidak ada pesanan aktif.");
            jComboBoxItemPesanan.setModel(model);
            btnHapus.setEnabled(false);
        }
    }
    
