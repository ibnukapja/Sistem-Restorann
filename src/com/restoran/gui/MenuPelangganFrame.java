    private void btnPilihMetodePembayaranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihMetodePembayaranActionPerformed
        if (currentPesanan == null || currentPesanan.getDetailPesananList().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Belum ada pesanan aktif untuk dipilih metode pembayarannya.", "Info Pembayaran", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    PilihMetodePembayaranFrame pilihMetodeFrame = new PilihMetodePembayaranFrame(currentPesanan);
    pilihMetodeFrame.setVisible(true);

    pilihMetodeFrame.addWindowListener(new WindowAdapter() {
        @Override
        public void windowClosed(WindowEvent windowEvent) {
            if (!pilihMetodeFrame.isCancelled()) {
                String metodeTerpilih = pilihMetodeFrame.getSelectedMetode();
                if (metodeTerpilih != null) {
                    currentPesanan.setMetodePembayaran(metodeTerpilih);

                    JOptionPane.showMessageDialog(null,
                                                  "Metode pembayaran terpilih: " + metodeTerpilih, 
                                                  "Metode Pembayaran", 
                                                  JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Tidak ada metode pembayaran yang dipilih.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Pemilihan metode pembayaran dibatalkan.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    });
    }//GEN-LAST:event_btnPilihMetodePembayaranActionPerformed

    private void btnEditPesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditPesananActionPerformed
        if (currentPesanan == null || currentPesanan.getDetailPesananList().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Belum ada pesanan untuk diedit.", "Info Edit Pesanan", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        EditPesananFrame editPesananFrame = new EditPesananFrame(currentPesanan);
        editPesananFrame.setVisible(true);

        editPesananFrame.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent windowEvent) {
                if (currentPesanan != null && !currentPesanan.getDetailPesananList().isEmpty()) {
                    JOptionPane.showMessageDialog(null,
                                              "Pesanan berhasil diupdate. Total item saat ini: " + currentPesanan.getDetailPesananList().size() + " item.",
                                              "Pesanan Diupdate",
                                              JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null,
                                              "Pesanan mungkin kosong atau item dihapus semua.",
                                              "Pesanan Diupdate",
                                              JOptionPane.WARNING_MESSAGE);
                }
            }
        });
    }//GEN-LAST:event_btnEditPesananActionPerformed

