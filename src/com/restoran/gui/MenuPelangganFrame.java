    private void btnHapusPesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusPesananActionPerformed
        if (currentPesanan == null || currentPesanan.getDetailPesananList().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Belum ada pesanan untuk dihapus.", "Info Hapus Pesanan", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        HapusPesananFrame hapusPesananFrame = new HapusPesananFrame(currentPesanan);
        hapusPesananFrame.setVisible(true);

        hapusPesananFrame.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent windowEvent) {
                if (currentPesanan != null && !currentPesanan.getDetailPesananList().isEmpty()) {
                    JOptionPane.showMessageDialog(null,
                                              "Pesanan berhasil diupdate. Sisa item: " + currentPesanan.getDetailPesananList().size() + " item.",
                                              "Pesanan Diupdate",
                                              JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null,
                                              "Semua item pesanan telah dihapus.",
                                              "Pesanan Kosong",
                                              JOptionPane.INFORMATION_MESSAGE);
                }
            }
        });
    }//GEN-LAST:event_btnHapusPesananActionPerformed

    private void btnOrderFixCetakStrukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderFixCetakStrukActionPerformed
        if (currentPesanan == null || currentPesanan.getDetailPesananList().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Belum ada item dalam pesanan.", "Error Finalisasi", JOptionPane.ERROR_MESSAGE);
        return;
    }
    if (currentPesanan.getMetodePembayaran().equals("Belum Dipilih")) {
        JOptionPane.showMessageDialog(this, "Pilih metode pembayaran terlebih dahulu.", "Error Finalisasi", JOptionPane.ERROR_MESSAGE);
        return;
    }
    if (currentPesanan.isFixed()) {
        JOptionPane.showMessageDialog(this, "Pesanan ini sudah difinalisasi dan struk sudah dicetak.", "Info Finalisasi", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    currentPesanan.setFixed(true);
    currentPesanan.setStatusPesanan("Selesai");

    boolean successSaveReport = LaporanPenjualanManager.tulisStrukString(currentPesanan.generateLaporanString());
    if (!successSaveReport) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan laporan penjualan.", "Error File", JOptionPane.ERROR_MESSAGE);
    }

    StrukPembayaranFrame strukFrame = new StrukPembayaranFrame(currentPesanan);
    strukFrame.setVisible(true);
    strukFrame.addWindowListener(new WindowAdapter() {
        @Override
        public void windowClosed(WindowEvent windowEvent) {
            JOptionPane.showMessageDialog(null, 
                                          "Pesanan telah selesai dan struk dicetak. Data pesanan Anda telah direset.", 
                                          "Order Selesai", 
                                          JOptionPane.INFORMATION_MESSAGE);
            currentPesanan = new Pesanan(); 
        }
    });
    }//GEN-LAST:event_btnOrderFixCetakStrukActionPerformed
    
