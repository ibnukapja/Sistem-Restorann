    private void btnPesanMakananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesanMakananActionPerformed
        PesanMakananFrame pesanMakananFrame = new PesanMakananFrame(currentPesanan);
        pesanMakananFrame.setVisible(true);
        
        pesanMakananFrame.addWindowListener(new java.awt.event.WindowAdapter() {
        @Override
        public void windowClosed(java.awt.event.WindowEvent windowEvent) {
            Pesanan pesananYangDibuat = pesanMakananFrame.getPesanan();
            if (pesananYangDibuat != null && !pesananYangDibuat.getDetailPesananList().isEmpty()) {
                currentPesanan = pesananYangDibuat;
                JOptionPane.showMessageDialog(null, 
                                              "Pesanan berhasil ditambahkan. Total item: " + currentPesanan.getDetailPesananList().size(), 
                                              "Pesanan Diperbarui", 
                                              JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, 
                                              "Tidak ada item yang ditambahkan ke pesanan.", 
                                              "Peringatan", 
                                              JOptionPane.WARNING_MESSAGE);
            }
        }
    });
    }//GEN-LAST:event_btnPesanMakananActionPerformed

